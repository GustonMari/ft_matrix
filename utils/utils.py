from colorama import Fore

def green(text):
    """Print green text."""
    print(Fore.GREEN + text + Fore.RESET)
    
def red(text):
    """Print red text."""
    print(Fore.RED + text + Fore.RESET)

def yellow(text):
    """Print yellow text."""
    print(Fore.YELLOW + text + Fore.RESET)

def blue(text):
    """Print blue text."""
    print(Fore.BLUE + text + Fore.RESET)